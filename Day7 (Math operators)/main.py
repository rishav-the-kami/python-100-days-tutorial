# Operators

a = 65
b = 32

print(a+b)  # 97
print(a-b)  # 33
print(a*b)  # 2080
print(a/b)  # 2.01325
print(a//b) # 2 (it also divides 65 by 32 but it removes the decimal places)
print(a%b)  # 1
print(2**5)  # ** is exponential operator. returns 2^5 = 32

# NOTE // (double slash) operator divides the number but removes the decimal places
# NOTE ** (double asterisk) Exponential operator finds the power...
