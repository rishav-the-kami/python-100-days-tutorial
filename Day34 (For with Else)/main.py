# NOTE u can use Else with For and While Loops too
for i in range(1, 6):
    print(i)
else:
    print("Loop Over!")

""" Output:
1
2
3
4
5
Loop Over!
"""

# so it means that after the loop has COMPLETED SUCCESSFULLY (COMPLETED SUCCESSFULLY is important), the else block will execute.



